     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nombre' => 'required',
            'stock' => 'required'
        ]);

        $venta = Venta::create($request->all());

        return redirect()->route('admin.ventas.index', compact('venta'))->with('info', 'La venta se registro correctamente.');
    }

